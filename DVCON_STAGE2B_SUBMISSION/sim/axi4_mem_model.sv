  // Write SM
  typedef enum logic [2:0] { W_IDLE, W_DATA, W_RESP } w_state_t;
  w_state_t w_state = W_IDLE;
  logic [63:0] curr_waddr;
  logic [11:0] saved_awid;

  always_ff @(posedge aclk or negedge aresetn) begin
    if (!aresetn) begin
      w_state <= W_IDLE;
      m_axi_awready <= 0;
      m_axi_wready <= 0;
      m_axi_bvalid <= 0;
      m_axi_bid <= 12'h0;
      m_axi_bresp <= 2'b00;
    end else begin
      case (w_state)
        W_IDLE: begin
          m_axi_bvalid <= 0;
          if (m_axi_awvalid) begin
            $display("[%0t ns] MEM MODEL: Write Address request. addr=%h, len=%d, id=%d", $time, m_axi_awaddr, m_axi_awlen, m_axi_awid);
            curr_waddr <= m_axi_awaddr;
            saved_awid <= m_axi_awid;
            m_axi_awready <= 1;
            w_state <= W_DATA;
          end else begin
            m_axi_awready <= 0;
          end
        end
        W_DATA: begin
          m_axi_awready <= 0;
          m_axi_wready <= 1;
          if (m_axi_wvalid && m_axi_wready) begin
            int idx = addr_to_idx(curr_waddr);
            $display("[%0t ns] MEM MODEL: Write Data beat. addr=%h, index=%d, data=%h, last=%b", $time, curr_waddr, idx, m_axi_wdata, m_axi_wlast);
            if (m_axi_wstrb[0]) mem[idx][7:0]   <= m_axi_wdata[7:0];
            if (m_axi_wstrb[1]) mem[idx][15:8]  <= m_axi_wdata[15:8];
            if (m_axi_wstrb[2]) mem[idx][23:16] <= m_axi_wdata[23:16];
            if (m_axi_wstrb[3]) mem[idx][31:24] <= m_axi_wdata[31:24];
            if (m_axi_wstrb[4]) mem[idx][39:32] <= m_axi_wdata[39:32];
            if (m_axi_wstrb[5]) mem[idx][47:40] <= m_axi_wdata[47:40];
            if (m_axi_wstrb[6]) mem[idx][55:48] <= m_axi_wdata[55:48];
            if (m_axi_wstrb[7]) mem[idx][63:56] <= m_axi_wdata[63:56];
            curr_waddr <= curr_waddr + 8;
            if (m_axi_wlast) begin
              m_axi_wready <= 0;
              m_axi_bvalid <= 1;
              m_axi_bid <= saved_awid;
              m_axi_bresp <= 2'b00;
              w_state <= W_RESP;
            end
          end
        end
        W_RESP: begin
          if (m_axi_bready && m_axi_bvalid) begin
            $display("[%0t ns] MEM MODEL: Write Response sent. id=%d", $time, m_axi_bid);
            m_axi_bvalid <= 0;
            m_axi_bid <= 12'h0;
            m_axi_bresp <= 2'b00;
            w_state <= W_IDLE;
          end
        end
      endcase
    end
  end

  // Read SM
  typedef enum logic [2:0] { R_IDLE, R_LATCH, R_DATA } r_state_t;
  r_state_t r_state = R_IDLE;
  logic [63:0] curr_raddr;
  logic [7:0]  curr_rlen;
  logic [7:0]  curr_rbeat;
  logic [11:0] saved_arid;
  int read_timer = 0;

  always_ff @(posedge aclk or negedge aresetn) begin
    if (!aresetn) begin
      r_state <= R_IDLE;
      m_axi_arready <= 0;
      m_axi_rvalid <= 0;
      m_axi_rlast <= 0;
      read_timer <= 0;
      m_axi_rdata <= 64'h0;
      m_axi_rid   <= 12'h0;
      m_axi_rresp <= 2'b00;
    end else begin
      case (r_state)
        R_IDLE: begin
          if (m_axi_arvalid) begin
            $display("[%0t ns] MEM MODEL: Read Address request. addr=%h, len=%d, id=%d", $time, m_axi_araddr, m_axi_arlen, m_axi_arid);
            curr_raddr <= m_axi_araddr;
            curr_rlen <= m_axi_arlen;
            saved_arid <= m_axi_arid;
            m_axi_arready <= 1;
            read_timer <= READ_LATENCY;
            r_state <= R_LATCH;
            curr_rbeat <= 0;
          end else begin
            m_axi_arready <= 0;
          end
        end
        R_LATCH: begin
          m_axi_arready <= 0;
          if (read_timer > 0) begin
            read_timer <= read_timer - 1;
          end else begin
            m_axi_rvalid <= 1;
            m_axi_rdata <= mem[addr_to_idx(curr_raddr)];
            m_axi_rid <= saved_arid;
            m_axi_rresp <= 2'b00;
            m_axi_rlast <= (curr_rbeat == curr_rlen);
            r_state <= R_DATA;
          end
        end
        R_DATA: begin
          if (m_axi_rvalid && m_axi_rready) begin
            $display("[%0t ns] MEM MODEL: Read Data beat. addr=%h, index=%d, data=%h, last=%b", $time, curr_raddr, addr_to_idx(curr_raddr), m_axi_rdata, m_axi_rlast);
            if (m_axi_rlast) begin
              m_axi_rvalid <= 0;
              m_axi_rlast <= 0;
              m_axi_rdata <= 64'h0;
              m_axi_rid <= 12'h0;
              m_axi_rresp <= 2'b00;
              r_state <= R_IDLE;
            end else begin
              curr_raddr <= curr_raddr + 8;
              curr_rbeat <= curr_rbeat + 1;
              m_axi_rdata <= mem[addr_to_idx(curr_raddr + 8)];
              m_axi_rlast <= ((curr_rbeat + 1) == curr_rlen);
            end
          end
        end
      endcase
    end
  end

